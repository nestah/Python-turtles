from turtle import *
bgcolor('#315671')
speed(3)
def Draw():

    up()
    pensize(4)
    home()
    fd(110)
    seth(90)
    down()
    begin_fill()
    color('black', '#fff')
    circle(110)
    end_fill()

    up()
    setpos(0,0)
    fd(30)
    seth(180)
    down()
    begin_fill()
    color('black')
    circle(30)
    end_fill()

    home()
    pensize(3)
    goto(38,38)
    seth(-40)
    down()
    begin_fill()
    color('black')
    circle(30, steps=5)
    end_fill()

    home()
    pensize(3)
    goto(-38,38)
    seth(40)
    down()
    begin_fill()
    color('black')
    circle(30, steps=5)
    end_fill()

    home()
    pensize(3)
    goto(-55,-10)
    seth(109)
    down()
    begin_fill()
    color('black')
    circle(30,steps=5)
    end_fill()

    home()
    pensize(3)
    goto(55, -10)
    seth(-109)
    down()
    begin_fill()
    color('black')
    circle(30, steps=5)
    end_fill()

    home()
    pensize(3)
    goto(0,-55)
    seth(-180)
    down()
    begin_fill()
    color('black')
    circle(30, steps=5)
    end_fill()


    up()
    home()
    fd(80)
    seth(90)
    down()
    circle(80)

up()
goto(-90,-150)
